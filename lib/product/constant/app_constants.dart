2. 1 "alıcı", "satıcı"lar tarafından satıma arz edilen ürün ve hizmetlerin ayıplı olup olmaması, listelenmesi, satışa arzı yasaklı ürünlerden olup olmaması, niteliği, orijinalliği, ürünün tanıtımında kullanılan yazılı ve/veya görüntülü açıklamaların doğruluğu ve tamlığı da dahil olmak üzere, ürün ve hizmetle ilgili hiçbir konu hakkında sıfıraracal’in bilgi sahibi olmadığı ve olması gerekmediğini ve bunları taahhüt ve garanti etmek yükümlülüğü bulunmadığını kabul ve beyan eder. 

2. 2 "alıcı", "satıcı"ya havale edilmesi için "sıfıraracal’in güvenli hesabı"na gönderdiği satış bedelinin kdv dahil hizmet bedeli komisyonu kesildikten ve "alıcı" tarafından bu sözleşmenin 2. 1. 5. Maddesinde belirtilen yükümlülüklerini yerine getirdikten sonra "satıcı"nın "sıfıraracal"e bildirdiği ve sadece türkiye sınırları içerisindeki bir banka hesabına transfer edileceğini, bu süreçte "sıfıraracal’in güvenli hesabı"nda bulunan "alıcı" ve/veya "satıcı" nın parası üzerinde "sıfıraracal"in kendi adına hiçbir işlem ve tasarrufta bulunmadığını kabul ve beyan eder. 

2. 2 satıcıların hak ve yükümlülükleri

2. 2. 1 "satıcı" satıma arz ettiği ürün ve hizmetler üzerinde çekişmesiz bir şekilde mutlak olarak tasarrufta bulunma hak ve yetkisine sahip olduğunu; bahsi geçen ürün ve hizmetlerin kendisi tarafından satılmasına karşı herhangi bir hukuki veya fiili engel bulunmadığını kabul ve taahhüt eder. "satıcı" satıma arz ettiği ürün ve hizmetlerin satışı, ilanı yasak ürünler de dâhil olmak üzere bu sözleşme hükümlerince belirlenen veya "portal"ın ilgili yerlerinde belirtilen "portal"ın ve "portal"da sunulan hizmetlerin kullanımına ilişkin kurallara ve üyeliğe ilişkin koşullara aykırı olmadığını ve yürürlükteki tüm mevzuata uygun olduğunu kabul ve taahhüt eder. "satıcı" satışa arz ettiği ürün ve hizmetlerin tüm hukuki sorumluğunun kendisine ait olduğunu "sıfıraracal"in ürün ve hizmetlerin arzı ve satışı konusunda, hiçbir sıfat altında kendisiyle doğrudan ve dolaylı ilişki içinde olmadığını kabul eder. 

2. 2. 2 "satıcı", satıma arz ettiği ürün ve hizmetle ilgili satın alma talebinde bulunan "alıcı"ya söz konusu ürün ve hizmetin satışını ilanında belirttiği şekilde gerçekleştireceğini kabul ve taahhüt eder. 

2. 2. 3 "satıcı", "alıcı"nın "sıfıraracal’in güvenli hesabı"na parayı göndermesi halinde "alıcı"ya ürün ve/veya hizmetin satışını gerçekleştireceğini ve ürün ve/veya hizmetin teslimini "alıcı" ile kararlaştırdıkları ve bu sözleşmenin ayrılmaz bir parçası olan ek-1’de belirtilen süreç ve sürelere uygun olarak tam ve eksiksiz bir şekilde ifa edeceğini kabul ve taahhüt eder. 

2. 2. 4 "satıcı", satışa arz ettiği ve ilanında belirttiği ürün veya hizmetlerin yasaklı ürünlerden olmasından, ürün veya hizmetin üretilmesinin, çoğaltılmasının, satılmasının, dağıtılmasının veya herhangi bir şekilde satışa arzına ilişkin bir fiilde bulunulmasının yürürlükteki herhangi bir düzenlemeye aykırı olmasından dolayı doğabilecek her türlü hukuki, ticari ve idari talebe karşı sorumlu olduğunu, bu yönde "sıfıraracal"e yapılan taleplere karşı sıfıraracal’i her türlü adli, ticari ve idari makam önünde savunma yükümlülüğü olduğunu kabul, beyan ve taahhüt eder. 

2. 2. 5 "satıcı", bu sözleşmenin 2. 2. 2. Maddesi doğrultusunda satma yükümlülüğü altına girdiği ürünlerin bedellerinin, sıfıraracal tarafından yönetilen "sıfıraracal’in güvenli hesabı"na "alıcı" ve "satıcı" arasındaki alım-satım işlemi yani ürün veya hizmetin “satıcı” tarafından fiziki olarak teslimi ve "alıcı" tarafından kabulü tamamlanana kadar bloke edilmek üzere "alıcı" tarafından iletileceğini kabul ve beyan eder. "satıcı", "alıcı"nın ürünü teslim alıp, "sıfıraracal’in güvenli hesabı"ndaki ürün bedelinin "satıcı"nın hesabına iletilmesi için sıfıraracal’e bildirimde bulunabileceğini veya "satıcı" tarafından kendisine gönderilen ürün veya hizmeti kabul etmeyerek ürün veya hizmeti "satıcı"ya iade edebileceğini ürün veya hizmet bedelinin kendisine iade edilmesi için sıfıraracal’e bildirimde bulunabileceğini kabul ve taahhüt eder. "satıcı", "alıcı"nın yukarıda belirtilen yükümlülüklerini yerine getirmemesi halinde sıfıraracal’in, "satıcı"nın "alıcı"ya yolladığı ürün ve hizmeti "alıcı"nın kabul etmiş olduğunu varsayacağını ve "sıfıraracal’in güvenli hesabı"nda bulunan söz konusu mala ilişkin ürün bedelinin herhangi bir uyarıya gerek kalmadan "alıcı" tarafından "satıcı"ya iletilmesi için sıfıraracal’e bildirimde bulunulmuş sayılacağını kabul ve beyan eder. "satıcı" bu madde içerisinde belirtilen işlemleri bu sözleşmenin ayrılmaz bir parçası olan ek-1’deki süreçlere, “portal” dahilinde konuyla ilgili yapılan açıklamalara ve surelere uygun olarak yerine getireceğini kabul, beyan ve taahhüt eder. 

2. 2. 6 "satıcı", "alıcı"ya karşı bu sözleşme uyarınca ürün veya hizmeti zamanında ve satışa arzında belirttiği niteliklere uygun ve ayıptan arî olarak göndereceğini ve teslimini sağlayacağını; ürün veya hizmetin "alıcı"ya satımı, mülkiyetinin ve ilgili haklarının devri ile yükümlülüklerin sadece kendisine ait olduğunu kabul eder. ”satıcı” bu madde içerisinde belirtilen gönderme ve teslim yükümlülüğünü, “sıfıraracal” tarafından onaylanan kargo firmaları aracılığıyla yerine getireceğini, gönderi ile ilgili bilgileri “sıfıraracal üye hesabı” üzerinden “portal”a gireceğini, bu yükümlülükleri yerine getirmeden gönderi veya teslim yapması halinde, “alıcı” tarafından "sıfıraracal’in güvenli hesabı"na gönderilen bedelin kendisine iletilmeyeceğini ve “alıcı”ya iade edileceğini kabul, beyan ve taahhüt eder. 

2. 2. 7 "satıcı", "alıcı"nın kendisine havale edilmesi için "sıfıraracal’in güvenli hesabı"na gönderdiği satış bedelinin kdv dahil hizmet bedeli komisyon kesildikten ve "alıcı"nın bu sözleşmenin 2. 1. 5. Maddesinde belirtilen yükümlülüklerini yerine getirdikten sonra "satıcı"nın "-sıfıraracal’e bildirdiği ve sadece türkiye sınırları içerisindeki bir banka hesabına transfer edileceğini, bu süreçte ""sıfıraracal’in güvenli hesabı"nda bulunan "alıcı" ve/veya "satıcı"nın parası üzerinde sıfıraracal’in kendi adına hiçbir işlem ve tasarrufta bulunmadığını kabul ve beyan eder. 

 

3. Listeleme hizmetleri

3. 1 bu “hizmet” “üye”lerin “sıfıraracal üyelik hesabı” üzerinden girmiş oldukları ilan içeriklerinin “sıfıraracal” “portal”ında yer alan kategoriler altında görüntülenmesinin sağlanmasıdır. Listeleme; “sıfıraracal” “portal”ının dinamik alt yapısı üzerinden “kullanıcı”ların ilgili kategoriler altında özelleştirdikleri sorguları doğrultusunda “üye”lere ait ilanların “kullanıcı” tarafından “sıfıraracal” tarafından belirlenen sıra dahilinde “sıfıraracal” ara yüzü üzerinden görüntülenmesidir. Listeleme; “sıfıraracal” veritabanına gelen sorguların “sıfıraracal” veritabanı üzerinde çalışan bilgisayar programının döneceği otomatik yanıtların “kullanıcı”ya gösterilmesi esasına dayanır. Bu sorgu neticesinde görüntülenen ilanlar, “üye”ler bu dokümanda belirtilen doping hizmetlerini seçmemiş iseler; otomatik olarak listelenir. “üye”ler ilanların listelemede farklı şekilde (sorgu sırası; ilanların okunurluğu, ilanların içeriği v. B yönlerden) yer almasını isterlerse; bu dokümanda yer alan doping hizmetlerinden yararlanmak durumundadırlar. 

3. 2 i̇lanlarının hangi kategori altında görüntüleneceğini(n) seçimi öncelikle “üye”ye aittir. “üye”nin belirlediği kategorinin ilanının konusuyla ilişkili olmaması durumunda “sıfıraracal” ilgili ilanının konusuyla ilişkili olan kategori altında görüntülenmesini “üye”ye herhangi bir bildirimde bulunmadan sağlayabilir. 

3. 3 “sıfıraracal” “portal”ında görüntülenecek ilanların kaç adetinin ücretsiz olacağı; kaç adetinin ise ücretli olacağını belirlemek hak ve yetkisi “sıfıraracal”e aittir. “sıfıraracal” bu yetkisini dilediği zaman kullanabilir. “üye”nin “portal”da görüntülenen ücretsiz ilanları “sıfıraracal”in “üye”ye yapacağı bildirim neticesinde ücretli olarak görüntülenmesi sağlanabilir. Bu durumda “üye” “sıfıraracal” tarafından kendisine ücretli olacağı bildirilen mevcut ilanları için “sıfıraracal” tarafından kendisine bildirilen süre içinde ücret ödeyerek ilanların aynı hüküm ve koşullar çerçevesinde “portal” üzerinden görüntülenmesini sağlayabilir. “üye” “sıfıraracal”in belirleyeceği süre içerisinde ilgili ilanlara ilişkin ücreti ödemez ise “sıfıraracal” bu sürenin bitiminde ücretli olacağını “üye”ye bildirdiği ilanların “portal” dahilinde görüntülenmesini durdurabilir. 

 

4. Mağaza sayfaları hizmetleri

4. 1 “üye”lerin “sıfıraracal üyelik hesabı” üzerinden girmiş oldukları ilan içeriklerinin sıfıraracal tarafından belirlenen ücret karşılığında “üye”ler tarafından “mağaza” adı altında oluşturulacak sayfalar toplu olarak gösterilmesini sağlayan hizmettir. 

4. 2 mağaza sayfaları hizmetlerinin kullanım ve yararlanma koşulları portalın ilgili bölümünde “üye”lere yönelik açıklamalarda belirtilir. “üye”ler bu açıklamalarda belirtilen koşullara uymakla mükelleftir. 

5. Doping

5. 1 üye”ler portal dahilinde yayınlanan ilanlarının diğer ilanlardan farklı biçimlerde portal kullanıcıları tarafından daha kolay ulaşılabilmesi, ilanlarının görsel etkinliğinin arttırılabilmesi gibi amaçlarla “sıfıraracal” tarafından sunulan ücrete tabi bir takım (doping) hizmetlerinden yararlanabilirler. 

 

5. 2 bu “hizmet” sayesinde “üye”ler “sıfıraracal üyelik hesabı” üzerinden girmiş oldukları ilan içeriklerinin listelemede farklı biçimlerde yer almasının sağlayabilirler. İçeriği ve ücreti sıfıraracal tarafından belirlenen dopinglerden birinin ya da birkaçının üye tarafından ilanının oluşturulması sırasında seçilmiş olması halinde, söz konusu ilan; üye tarafından seçilmiş olan doping ya da dopinglerin özelliklerine göre, listelemede aynı doping ya da dopinglerin seçilmemiş olduğu ilanlardan farklı biçimlerde görüntülenecektir. Doping sadece seçilmiş olduğu ilana aittir,üye tarafından seçilmiş olduğu süre bitiminden önce yada sonra farklı bir ilana aktarılamaz, dopingin sıfıraracal kaynaklı sayfa hatalarından yada sıfıraracal veri tabanındaki sorunlardan kaynaklı nedenlerle görüntülenmesinde doğacak süre kaybı sıfıraracal tarafından dopingin süresine sorunlu günler kadar ek yapılmak koşulu ile giderilir. 

 

5. 3 doping içerik ve ücretleri sıfıraracal tarafından belirlenir. Sıfıraracal söz konusu içerik ve ücretleri “portal”da üyelere duyurulmak kaydıyla istediği zaman değiştirme hak ve yetkisine sahiptir. Dopingi yayında olan ilanın üye tarafından yayından kaldırılması yada bağlı bulunduğu ilanın sitekurallarına aykırı olduğu durumlarda sıfıraracal ilanı ve dopingi yayından kaldırır ve ilan sahibine (üye’ye) ödeme iadesi yapmaz. 

5. 4 hediye dopinglerin ücretlendirilmesi şartı: kullanıcının mağazayı kapatma isteği halinde ya da mağaza kurallarının ihlali durumunda mağaza kullanım hakkının sıfıraracal tarafından sona erdirilmesi durumunda; sisteme hediye ya da başka adlar altında olsa bile ücretsiz olarak tanımlanmış olan dopinglerin kullanılmış olanlarına karşılık olan ücret de mağaza kapatma isteği ya da mağaza kullanım hakkının sona erdirilmesi anındaki birim bedelinden hesaplanarak mağaza bedelinden kesilir ve mağaza bedeli bu bedeller kesilmiş olarak iade edilir. 

