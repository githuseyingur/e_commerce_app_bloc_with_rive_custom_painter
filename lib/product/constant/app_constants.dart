2. 1. 2 "güvenli elektronik ticaret hizmeti"nde "satıcı"; eğer ürünün belli fiyat aralıkları içerisinde "alıcı" tarafından verilecek teklifler neticesinde kesin satış fiyatının belirlenmesine ilişkin bir satış yöntemi öngörmüşse, "teklif ver" butonunu kullanarak, "alıcı" ürünü satın almak istediği fiyatı belirterek "satıcı"ya teklifini yapar. "alıcı"nın yapacağı bu teklif "satıcı"nın belirlediği teklif aralıkları içerisinde yapılmak zorundadır. "satıcı"nın "alıcı"nın teklifini kabul etmesi ile taraflar arasında sözleşme kurulmuş olur. Kabul beyanı sistem üzerinden "alıcı"nın elektronik posta adresine gönderilir ve eş zamanlı olarak "alıcı"nın "sıfıraracal üyelik hesabı"nda görüntülenir. "satıcı" kabul veya red beyanını "sıfıraracal üyelik hesabı"nı kullanarak yapmak zorundadır. "sıfıraracal üyelik hesabı" kullanılmadan yapılan kabul veya red beyanlarına ilişkin "sıfıraracal"in hiçbir sorumluluğu yoktur. "satıcı" tarafından "alıcı"nın verdiği teklifin bu maddede belirtilen usulle kabul edilmesinden 3 (üç) işgünü içerisinde taraflarca anlaşılan ürün bedelinin "alıcı" tarafından sıfıraracal’in "güvenli hesabı"na yatırılmaması durumunda, taraflar arasındaki anlaşma kendiliğinden fesholunmuş addedilecektir. "güvenli elektronik ticaret hizmeti"nde "satıcı" ürünün fiyatını belirleyerek ilan içerisinde ilgili bölümde belirtmişse, “alıcı”nın sepete ekleme işleminden sonra ürünle ilgili ödeme işlemini gerçekleştirmesi ile taraflar arasında sözleşme kurulmuş olur. 

2. 1. 3 "alıcı", portal" üzerinden "sıfıraracal" internet portalına ait alt yapıyı kullanarak yapmış olduğu satın alma talepleri ile "satıcı" tarafından belirlenmiş satış şartlarını ve usullerini de kabul etmiş sayılır. İstisnai durumlar hariç, "alıcı" tarafından yapılan satın alma talepleri geri çekilemez veya iptal edilemez. Bu sözleşme kapsamında istisnai durumlar 2. 1. 2 (son) maddesinde belirtilen teklif bedelinin suresi içerisinde "sıfıraracal güvenli hesabi"na yatırılmaması,"satıcı"nın kimliğinin belirli olmaması, satıma konu olan ürünün bu sözleşmenin . 2. 1. 1 ve 2. 1. 2 maddelerinde belirtilen şekilde taraflar arasında sözleşmenin akdedilmesinden itibaren satıcı tarafından belirtilen süre zarfında "alıcı"ya ulaşmaması olarak tanımlanmıştır. 

2. 1. 4 "alıcı", "portal" üzerinden "sıfıraracal"in internet portalına ait alt yapıyı kullanarak yapmış olduğu satın alma taleplerinde yalnızca, "sıfıraracal’in güvenli hesabı"na para transferini yaparak para borcunun ifasını gerçekleştirebilir. Para borcunun ifasının "sıfıraracal’in "güvenli hesabı" dışında başka şekilde gerçekleştirilmesi durumunda; sıfıraracal’in bu sözleşmede belirtilen "güvenli elektronik ticaret hizmeti"ne ilişkin hiçbir sorumluluğu bulunmadığı gibi; "alıcı" ve "satıcı"nın hak ve borçları da bu sözleşmede belirtilen hükümlere tabi değildir. Sıfıraracal, para borcunun ifasında anlaşmalı bankalarla hareket ederek "alıcı"ya taksitle ödeme kolaylığı sunabilir. Bu durumda ilgili taksitli ödemeye ilişkin koşullar, "sıfıraracal" tarafından ödemenin gerçekleştirilmesi sırasında "portal"ın ilgili bölümünden "alıcı"ya duyurulmaktadır. "alıcı" taksitli ödemeye karar verir ise, "portal"ın ilgili bölümünde belirtilen koşulları da kabul etmiş olmaktadır. 

2. 1. 5 "alıcı", bu sözleşmede belirtilen koşullar dahilinde "satıcı" ile aralarındaki satış akdinin ödeme kısmının ifası amacıyla “sıfıraracal’in, güvenli hesabı"na, bu sözleşmenin ayrılmaz bir parçası olan ek-1’deki süreç tamamlanana kadar bloke edilmek üzere gönderilecektir. "alıcı", "satıcı"ya ürünün gönderi bilgilerini göndermesinden sonra ürünü teslim alıp "güvenli hesap"taki ürün bedelinin "satıcı"nın hesabına transferi için "sıfıraracal üyelik hesabı" üzerinden bildirimde bulunacağını veya "satıcı" tarafından kendisine gönderilen ürünü kabul etmemesi halinde ise, ürünü "satıcı"ya iade edeceğini ve ürün bedelinin kendisine iade edilmesi için "sıfıraracal üyelik hesabı" üzerinden bildirimde bulunacağını kabul ve taahhüt eder. "alıcı" ürünü "satıcı"ya herhangi bir nedenle iade etmek üzere geri gönderdiği takdirde "sıfıraracal’i̇n güvenli hesabı"nda tutulan ilgili ürüne ait satış bedeli, ancak "satıcı"nın ürünü geri aldığına dair " sıfıraracal üyelik hesabı" üzerinden bildirimde bulunması halinde "alıcı"nın hesabına geri gönderilecektir. “alıcı” tarafından bu madde içerisinde belirtilen şekilde onay verilmesi halinde; verilen  onay hiçbir şekilde onay geri alınamayacak, bu süreçten sonra ortaya çıkabilecek herhangi bir uyuşmazlık ve benzeri durumda “sıfıraracal”, “üye”ler arasındaki iletişimi sağlama da dahil olmak üzere herhangi bir yükümlülük altında bulunmayacaktır. "alıcı" yukarıda belirtilen yükümlülüklerini yerine getirmediği takdirde, "sıfıraracal" tarafından "satıcı"nın kendisine yolladığı ürünü kabul etmiş sayılacağını ve "sıfıraracal güvenli hesabı"nda bulunan söz konusu mala ilişkin ürün bedelinin herhangi bir uyarıya gerek kalmadan "satıcı"ya transferi için "sıfıraracal üyelik hesabı" üzerinden bildirimde bulunmuş sayılacağını kabul ve beyan eder. "alıcı” bu madde içerisinde belirtilen işlemleri bu sözleşmenin ayrılmaz bir parçası olan ek-1’deki süreçlere, portal dahilinde konuyla ilgili yapılan açıklamalara ve sürelere uygun olarak yerine getireceği